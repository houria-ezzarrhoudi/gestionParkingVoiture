    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Voiture voiture = voitureService.findById(jTextField6.getText(), voitures);
        if (voiture == null) {
            JOptionPane.showMessageDialog(null, "cette voiture non trouvee", "ERREUR", JOptionPane.ERROR_MESSAGE);
            jTextField6.setText("");

        } else {
            jTextField3.setText(voiture.getMatricule());
            jTextField7.setText(voiture.getMarque());
            jTextField8.setText(voiture.getModel());
            jTextField9.setText(voiture.getCategorie());
            JOptionPane.showMessageDialog(null, "cette voiture existante", "INFO", JOptionPane.INFORMATION_MESSAGE);
            jTextField6.setText("");
            jTextField3.setText("");
            jTextField7.setText("");
            jTextField8.setText("");
            jTextField9.setText("");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

