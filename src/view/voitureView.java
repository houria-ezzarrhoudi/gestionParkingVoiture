    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Voiture voiture = new Voiture();
        voiture.setId(jTextField2.getText());
        voiture.setMatricule(jTextField1.getText());
        voiture.setMarque(jTextField4.getText());
        voiture.setModel(jTextField5.getText());
        if (jCheckBox1.isSelected() == true) {
            voiture.setCategorie("C:Normal");
        } else if (jCheckBox2.isSelected() == true) {
            voiture.setCategorie("B:Mediem");
        } else if (jCheckBox3.isSelected() == true) {
            voiture.setCategorie("A:High");

        }

        int resultat = voitureService.save(voiture, voitures);
        if (resultat > 0) {

            JOptionPane.showMessageDialog(null, "Bonjour L'enregistrement de cette voiture est fait avec succee ", "info", JOptionPane.INFORMATION_MESSAGE);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jCheckBox1.setSelected(false);
            jCheckBox2.setSelected(false);
            jCheckBox3.setSelected(false);
        } else {
            JOptionPane.showMessageDialog(null, "Bonjour l'enregistrement est impossible ", "erreur", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jCheckBox1.setSelected(false);
            jCheckBox2.setSelected(false);
            jCheckBox2.setSelected(false);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

